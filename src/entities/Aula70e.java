package entities;

public class Aula70e {
	private String nome;
	private double preco;
	
	public Aula70e(String nome, double preco) {
		this.nome = nome;
		this.preco = preco;
		
	}

	public String getNome() {
		return nome;
	}

	public double getPreco() {
		return preco;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public void setPreco(double preço) {
		this.preco = preco;
	}
	
	

}
